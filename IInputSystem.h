class IInputSystem
{
public:
	void EnableInput(bool state)
	{
		using orig_fn = void(__thiscall*)(IInputSystem*, bool);
		return (*(orig_fn**)this)[8](this, state);
	}//8

	void ResetInputState()
	{
		using orig_fn = void(__thiscall*)(IInputSystem*);
		return (*(orig_fn**)this)[26](this);
	}
	//26
};